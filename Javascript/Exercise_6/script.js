let day = prompt("Enter the day of the week");

switch (day) {
  case "Monday":
    alert("Montag");
    break;

  case "Tuesday":
    alert("Dienstag");
    break;

  case "Wednesday":
    alert("Mittwoch");
    break;

  case "Thursday":
    alert("Donnerstag");
    break;

  case "Friday":
    alert("Freitag");
    break;

  case "Saturday":
    alert("Samstag");
    break;

  case "Sunday":
    alert("Sonntag");
    break;

  default:
    alert("invalid input")
    break;
}